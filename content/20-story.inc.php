     <h1>Looking to the future with LibreNMS</h1>
     <p>The LibreNMS community has been growing rapidly in recent months, and we'd like your help to hold a one-day gathering in London on Sunday, 30 August 2015.</p>
     <p></p>
     <p>We aim to:</p>
     <ul>
      <li>Bring together the core team of Daniel, Neil, and Paul.</li>
      <li>Extend an open invitation to any community members to join us.</li>
      <li>Work together on requirements, planning, design, and coding to ensure that LibreNMS continues to meet its users' needs into the future.</li>
      <li>Have a great time getting to know each other in person!</li>
     </ul>
     <p>We think a network monitoring system based on the principles of Free Software and guided by the community which uses it has an important part to play in the Internet world. We've already made huge strides as a community, and a gathering like this will improve both the community and technical aspects of LibreNMS for everyone.</p>
